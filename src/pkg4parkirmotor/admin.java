    public void showData(){
        
        String[] kolom = {"NO",  "ID", "PlatNomor", "NamaPemilik", "Kelas"};
        
        dtm = new DefaultTableModel(null, kolom);
        try{
            Statement stat = konek.createStatement();
            String query = "SELECT * FROM data_motor";
            ResultSet rs = stat.executeQuery(query);
            
            int no = 1;
            
            while (rs.next()){
                String id = rs.getString("id_motor");
                String pn = rs.getString("platnomor");
                String nama = rs.getString("nama_pemilik");
                String kelas = rs.getString("kelas");
                
                dtm.addRow(new String[]{no+"", id, pn, nama, kelas});
                no++;
            }
        } catch (SQLException e){
            e.printStackTrace();
        }
        tblmotor.setModel(dtm);
    }

    

